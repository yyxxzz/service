package com.yoho.gateway.model.user.sign;

public class UserSignInfoVO {
	
	private String dateKey;
	private String constantDay;
	private String yohoCoinNum;
	private String signed;
	private String today;

	public String getDateKey() {
		return dateKey;
	}

	public void setDateKey(String dateKey) {
		this.dateKey = dateKey;
	}

	public String getConstantDay() {
		return constantDay;
	}

	public void setConstantDay(String constantDay) {
		this.constantDay = constantDay;
	}

	public String getYohoCoinNum() {
		return yohoCoinNum;
	}

	public void setYohoCoinNum(String yohoCoinNum) {
		this.yohoCoinNum = yohoCoinNum;
	}

	public String getSigned() {
		return signed;
	}

	public void setSigned(String signed) {
		this.signed = signed;
	}

	public String getToday() {
		return today;
	}

	public void setToday(String today) {
		this.today = today;
	}

}
